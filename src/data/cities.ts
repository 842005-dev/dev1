export const getFilteredCities = (searchTerm: string, limit: number = 10): string[] => {
  if (!searchTerm.trim()) return [];
  
  const term = searchTerm.toLowerCase();
  
  // First, find exact matches at the beginning (highest priority)
  const exactMatches = cities.filter(city => 
    city.toLowerCase().startsWith(term)
  );
  
  // Then, find cities that contain the search term but don't start with it
  const containsMatches = cities.filter(city => 
    city.toLowerCase().includes(term) && !city.toLowerCase().startsWith(term)
  );
  
  // Special handling for Indian cities - prioritize them if searching for Indian terms
  const indianCityTerms = ['india', 'indian', 'mumbai', 'delhi', 'bangalore', 'chennai', 'kolkata', 'hyderabad'];
  const isIndianSearch = indianCityTerms.some(indianTerm => term.includes(indianTerm) || indianTerm.includes(term));
  
  if (isIndianSearch) {
    const indianCities = [...exactMatches, ...containsMatches].filter(city => city.includes('India'));
    const otherCities = [...exactMatches, ...containsMatches].filter(city => !city.includes('India'));
    return [...indianCities, ...otherCities].slice(0, limit);
  }
  
  // Combine and limit results
  return [...exactMatches, ...containsMatches].slice(0, limit);
};